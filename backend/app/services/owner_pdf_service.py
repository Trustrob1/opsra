_BRAND_TEAL = "#0d9488"
_GENERATED_FOOTER = "Generated by Opsra"

_STAGE_ORDER = [
    "new", "contacted", "meeting_done", "proposal_sent",
    "converted", "lost", "not_ready",
]
_SCORE_ORDER = ["hot", "warm", "cold", "unscored"]


# ── Shared helpers ───────────────────────────────────────────────────────────

def _human_range(date_from: Optional[date], date_to: Optional[date]) -> str:
    if not date_from or not date_to:
        return "All time"
    if date_from == date_to:
        return date_from.strftime("%-d %b %Y")
    return f"{date_from.strftime('%-d %b %Y')} \u2013 {date_to.strftime('%-d %b %Y')}"


def _prior_period(date_from: date, date_to: date) -> tuple[date, date]:
    """
    Returns the immediately preceding period of equal length, e.g.
    1-30 Jun -> 1-31 May (30-day window shifted back 30 days).

    Used only by build_comparison_pdf. The pdf_report routing schema (see
    OWNER-PDF-1 spec) carries a single date_from/date_to even when
    report_type=comparison — unlike the separate "compare" action in
    owner_query_service.py, which resolves explicit period_a/period_b via
    the Sonnet routing call. This formatter treats date_from/date_to as the
    current period and derives the prior period itself.
    """
    span = (date_to - date_from).days + 1
    prior_to = date_from - timedelta(days=1)
    prior_from = prior_to - timedelta(days=span - 1)
    return prior_from, prior_to


def _stage_sort_key(stage: str) -> int:
    try:
        return _STAGE_ORDER.index(stage)
    except ValueError:
        return len(_STAGE_ORDER)


def _score_sort_key(score: str) -> int:
    try:
        return _SCORE_ORDER.index(score)
    except ValueError:
        return len(_SCORE_ORDER)


def _money(value: Any) -> str:
    try:
        return f"\u20a6{float(value or 0):,.2f}"
    except Exception:
        return "\u20a60.00"


def _render_pdf(html: str) -> Optional[bytes]:
    """S14: returns None on any WeasyPrint failure."""
    try:
        from weasyprint import HTML
        return HTML(string=html).write_pdf()
    except Exception as exc:
        logger.error("owner_pdf_service._render_pdf failed: %s", exc)
        return None



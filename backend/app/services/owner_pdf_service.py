"""
app/services/owner_pdf_service.py
OWNER-PDF-1 — Owner WhatsApp PDF report formatters.

Four report types, provider-agnostic:
  build_period_summary_pdf()
  build_lead_pipeline_pdf()
  build_orders_fulfilment_pdf()
  build_comparison_pdf()

Each returns (pdf_bytes | None, meta: dict). meta always includes "label" on
success; on failure meta includes "reason" and pdf_bytes is None.

Design (per OWNER-PDF-1 spec):
  - Provider-agnostic: report adapts to whichever providers are connected.
  - Non-Shopify safe: all four report types work without Shopify.
  - Each new provider adds one _render_{provider}_section() function only.

Uses the same provider registry as owner_query_service.py — get_provider(),
get_connected_providers(), get_provider_capabilities() — and never reads
provider-owned data through any interface other than get_summary()/search(),
with one deliberate exception: the lead_pipeline full-record export needs
`assigned_to` (rep), which no provider .search()/.get_summary() response
currently carries. That one path queries `leads` + `users` directly, using
the exact same table/column shape OpsraOrdersProvider already relies on in
opsra_orders_provider_service.py (org_id/deleted_at/created_at filters,
full_name/whatsapp/phone/source/score/stage/last_activity_at columns).

PDF engine: WeasyPrint (already a project dependency — see
report_delivery_worker.py / RPT-1A).

S14: every formatter returns (None, {'reason': ...}) on failure, never raises.
Pattern 29: load_dotenv() at module level.
"""
